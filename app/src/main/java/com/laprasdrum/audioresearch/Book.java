package com.laprasdrum.audioresearch;

public class Book {

    public String title;
    public String author;
    public int    price;
    public String publisher;

    public Book() {
    }

    public Book(String title, String author, int price, String publisher) {
        this.title = title;
        this.author = author;
        this.price = price;
        this.publisher = publisher;
    }
}
